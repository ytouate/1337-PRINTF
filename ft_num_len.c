#include "ft_printf.h"

size_t	ft_num_len(long long nbr)
{
	size_t	i;

	i = 1;
	if (nbr < 0)
	{
		i += 1;
		nbr *= -1;
	}
	while (nbr > 9)
	{
		i++;
		nbr /= 10;
	}
	return (i);
}
